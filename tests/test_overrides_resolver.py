# number_format swap (xlsx) — resolver + membership
# ---------------------------------------------------------------------------
def _xlsx_profile() -> dict:
    prof = schema.build_envelope("xlsx", {"name": "t"})
    prof["provenance"]["shell"]["sha256"] = "xlsx-sha-1"
    prof["roles"] = {
        "_index": ["metric.value"],
        "metric.value": {
            "resolver": {"type": "number_format", "number_format": "0.00"},
            "status": "resolved",
            "confidence": 1.0,
        },
    }
    return prof


class NumberFormatSwapTest(unittest.TestCase):
    def _present_swap(self, prof: dict, role: str, mask: str) -> dict:
        ov = prof["rules"]["overrides"]
        ov["status"] = "present"
        ov["source_shell_sha256"] = prof["provenance"]["shell"]["sha256"]
        ov["number_format_swaps"] = {role: mask}
        return prof

    def test_swap_rewrites_mask_and_stays_typed(self):
        prof = self._present_swap(_xlsx_profile(), "metric.value", "#,##0")
        op = ProfileResolver(prof).resolve_role("metric.value", fallback=None)
        # The mask is swapped verbatim; the op stays a legal number_format op.
        self.assertEqual(op.resolver.get("type"), "number_format")
        self.assertEqual(op.resolver.get("number_format"), "#,##0")

    def test_swap_ignored_when_overrides_absent(self):
        prof = _xlsx_profile()  # absent overrides
        op = ProfileResolver(prof).resolve_role("metric.value", fallback=None)
        self.assertEqual(op.resolver.get("number_format"), "0.00")

    def test_swap_membership_against_shell(self):
        """A swap mask the shell uses passes; one it does not is an ERROR."""
        prof = self._present_swap(_xlsx_profile(), "metric.value", "#,##0")

        class _FakeShell:
            pass

        # Stub the shell readers: the shell uses {#,##0} but not {0%}.
        orig = cd._xlsx_number_format_masks
        cd._xlsx_number_format_masks = lambda shell: {"#,##0"}
        cd._xlsx_defined_names_orig = cd._xlsx_defined_names
        cd._xlsx_defined_names = lambda shell: set()
        try:
            ok = cd.check_override_targets(_FakeShell(), prof)
            self.assertEqual(ok, [])  # #,##0 is a used mask
            bad = self._present_swap(_xlsx_profile(), "metric.value", "0%")
            errs = cd.check_override_targets(_FakeShell(), bad)
            self.assertTrue(errs)
            self.assertEqual(errs[0].check, "override_targets_exist")
            self.assertEqual(errs[0].severity, schema.Severity.ERROR.value)
        finally:
            cd._xlsx_number_format_masks = orig
            cd._xlsx_defined_names = cd._xlsx_defined_names_orig
            del cd._xlsx_defined_names_orig


# ---------------------------------------------------------------------------
# check_override_targets — fail-closed
# ---------------------------------------------------------------------------
class CheckOverrideTargetsFailClosedTest(unittest.TestCase):
    def test_noop_when_overrides_absent(self):
        prof = _docx_profile()  # absent
        self.assertEqual(cd.check_override_targets(object(), prof), [])

    def test_reroute_to_undeclared_role_errors(self):
        prof = _present_reroute(_docx_profile(), "heading.9", "role.not.declared")
        errs = cd.check_override_targets(object(), prof)
        self.assertTrue(any(f.check == "override_targets_exist" for f in errs))
        self.assertTrue(all(f.severity == schema.Severity.ERROR.value for f in errs))

    def test_reroute_target_not_shell_backed_errors(self):
        """A declared target whose resolver target the shell does NOT prove ERRORs."""
        prof = _present_reroute(_docx_profile(), "heading.9", "heading.1")

        # The shell does NOT carry Heading1/Heading 1 (now-missing target).
        orig = cd._docx_style_keys
        cd._docx_style_keys = lambda shell: {"Normal"}
        try:
            errs = cd.check_override_targets(object(), prof)
            self.assertTrue(errs)
            self.assertEqual(errs[0].check, "override_targets_exist")
            self.assertEqual(errs[0].severity, schema.Severity.ERROR.value)
        finally:
            cd._docx_style_keys = orig

    def test_demo_clear_empty_inventory_rejects(self):
        """reject-on-empty: a demo_clear value with NO captured set is an ERROR."""
        prof = _docx_profile()
        ov = prof["rules"]["overrides"]
        ov["status"] = "present"
        ov["source_shell_sha256"] = prof["provenance"]["shell"]["sha256"]
        ov["demo_clears"] = ["Lorem ipsum"]
        # No comprehension / surface demo set ⇒ captured set is EMPTY ⇒ reject.
        errs = cd.check_override_targets(None, prof)  # shell=None ok for demo branch
        self.assertTrue(errs)
        self.assertEqual(errs[0].check, "override_targets_exist")
        self.assertEqual(errs[0].severity, schema.Severity.ERROR.value)

    def test_demo_clear_membership_passes_when_captured(self):
        prof = _docx_profile()
        # Surface a captured demo region marker so the value is a known demo string.
        prof["surface"] = {
            "docx": {"demo_region": {"instruction_markers": ["Lorem ipsum"]}}
        }
        ov = prof["rules"]["overrides"]
        ov["status"] = "present"
        ov["source_shell_sha256"] = prof["provenance"]["shell"]["sha256"]
        ov["demo_clears"] = ["Lorem ipsum"]
        self.assertEqual(cd.check_override_targets(None, prof), [])

    def test_reroute_present_passes_when_shell_backed(self):
        prof = _present_reroute(_docx_profile(), "heading.9", "heading.1")
        orig = cd._docx_style_keys
        cd._docx_style_keys = lambda shell: {"Normal", "Heading1", "Heading 1"}
        try:
            self.assertEqual(cd.check_override_targets(object(), prof), [])
        finally:
            cd._docx_style_keys = orig


# ---------------------------------------------------------------------------
# Overrides frozen to the shell
# ---------------------------------------------------------------------------
class OverridesFrozenToShellTest(unittest.TestCase):
    def test_reextract_drift_reverts_resolver(self):
        prof = _present_reroute(_docx_profile(), "heading.9", "heading.1")
        # Present + current ⇒ rerouted.
        live = ProfileResolver(prof).resolve_role("heading.9", fallback=None)
        self.assertEqual(live.resolver.get("style_id"), "Heading1")
        # A re-extract re-stamps provenance.shell.sha256 (store.save_profile): the
        # recorded sha no longer matches ⇒ the lesson is frozen out, behavior reverts.
        prof["provenance"]["shell"]["sha256"] = "re-extracted-sha"
        reverted = ProfileResolver(prof).resolve_role("heading.9", fallback=None)
        self.assertEqual(reverted.resolver, {})  # back to today's stub


